package Recurse;

public class BoxClassForBoxTower {
	int width;
	int height;
	int depth;
	
	public BoxClassForBoxTower(int _width, int _height, int _depth){
		this.width = _width;
		this.height = _height;
		this.depth = _depth;
	}
	
	public int getWidth(){
		return this.width;
	}
	public int getHeight(){
		return this.height;
	}
	public int getDepth(){
		return this.depth;
	}
}
